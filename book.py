class Book:
    def __init__(self, id=None, title=None, author=None, genres=None, year=None, pageNumber=None, cover=None, description=None, avgscore=None):
        self.id = id
        self.title = title
        self.author = author
        self.genres = genres
        self.year = year
        self.pageNumber = pageNumber
        self.cover = cover
        self.description = description
        self.avgscore = avgscore
